{
  /* USER CODE BEGIN DataToIhmMeth */
  /* Infinite loop */
	uint32_t i = 400;
	uint32_t z = 0;
	uint32_t e = 245;
	uint8_t stop[2] = "\n";

	for(;;)
	{
		itoa(i, txdata, 10);
		HAL_UART_Transmit(&huart2, txdata, sizeof(txdata), 100);
		itoa(z, txdata, 10);
		HAL_UART_Transmit(&huart2, txdata, sizeof(txdata), 100);
		itoa(e, txdata, 10);
		HAL_UART_Transmit(&huart2, txdata, sizeof(txdata), 100);

		HAL_UART_Transmit(&huart2, stop, sizeof(stop), 100);
		if (i > 1000){
			i=0;
		} else {
			i+=100;
		}

		if (z > 1000){
			z=0;
		} else {
			z+=100;
		}

		if (e > 1000){
			e=0;
		} else {
			e+=100;
		}
		osDelay(1500);
	}
  /* USER CODE END DataToIhmMeth */
}

